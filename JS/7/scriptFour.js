//Arithmetic operators in js
//Addition (+)
//Subtraction (-)
//Multiplication (*)
//Modulus Operator (%)
//Division (/)
//++ increment (add 1)
//-- decrement (subtract 1)
//exponentiation (**)

//+ operator
let a = 5;
let b = 2;
let c = a + b;
console.log(c);

//- operator
let d = 9;
let e = 3;
let f = d - e;
console.log(f);

//* multiplication
let k = 5;
let j = 2;
let h = k * j;
console.log(h);

//division operator
let l = 5;
let s = 2;
let q = l / s;
console.log(q);

//modulus operator
let g = 5;
let w = 2;
let r = g % w;
console.log(r);

//increment operator
let x = 4;
x++;
console.log(x);

//decrement operator
let y = 4;
y--;
console.log(y);

//exponentiation operator
let z = 2 ** 3;
console.log(z);
