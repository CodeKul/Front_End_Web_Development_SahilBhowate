let x = 6;

//basic syntax of switch case statement
switch (x) {
  case 1: //if x===1
    //Execute the statements for case#1
    break; //stop

  case 2: //if x===2
    //Execute the statements for case#2
    break; //stop

  case 3: //if x===3
    //Execute the statement for case#3
    break; //stop

  default:
    //Execute the statements
    break;
}
