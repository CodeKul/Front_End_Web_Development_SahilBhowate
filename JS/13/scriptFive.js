//[3] Conditional (ternary) operator
// Its a short hand version of if else statement
// condition ? expressionifTrue : expressionIfFalse
// operator operates on the operand.

let age = 26;

//Using the ternary operator
let whichStandard = age >= 17 ? "College" : "Junior College And School";

console.log("The value of whichStandard is ", whichStandard);
